package com.woniu.entity;

public class Fixed {
    public String id;
    public String userid;
    public String fixedlevel;
    public String fixedstatus;
    public String fixeddesc;
    public String fixedtitle;
    public String username;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getFixedlevel() {
        return fixedlevel;
    }

    public void setFixedlevel(String fixedlevel) {
        this.fixedlevel = fixedlevel;
    }

    public String getFixedstatus() {
        return fixedstatus;
    }

    public void setFixedstatus(String fixedstatus) {
        this.fixedstatus = fixedstatus;
    }

    public String getFixeddesc() {
        return fixeddesc;
    }

    public void setFixeddesc(String fixeddesc) {
        this.fixeddesc = fixeddesc;
    }

    public String getFixedtitle() {
        return fixedtitle;
    }

    public void setFixedtitle(String fixedtitle) {
        this.fixedtitle = fixedtitle;
    }
}
